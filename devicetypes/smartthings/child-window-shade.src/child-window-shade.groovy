def installed() {
	parent.windowShadeInstalled(device.deviceNetworkId)
}

def close() {
	parent.closeChild(device.deviceNetworkId)
}

def open() {
	parent.openChild(device.deviceNetworkId)
}

def pause() {
	parent.pauseChild(device.deviceNetworkId)
}

def setShadeLevel(level) {
	def currentShadeLevel = Integer.parseInt(device.currentState("shadeLevel").value)
	parent.setLevelChild(level, device.deviceNetworkId, currentShadeLevel)
}

